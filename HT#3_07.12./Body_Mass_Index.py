def calculate_bmi(height, weight, gender):
    bmi = weight / ((height / 100) ** 2)  #
    # bmi = f"{bmi:.2f}"
    return bmi

def get_bmi_category(bmi, gender):
    if gender == "male":
        if bmi < 18.5:
            return "Underweight / Недостаток веса"
        elif 18.5 <= bmi < 25:
            return "Normal weight / Нормальный вес"
        elif 25 <= bmi < 30:
            return "Overweight / Избыточный вес"
        else:
            return "Obesity / Ожирение"
    elif gender == "female":
        if bmi < 17:
            return "Underweight / Недостаток веса"
        elif 17 <= bmi < 24:
            return "Normal weight / Нормальный вес"
        elif 24 <= bmi < 29:
            return "Overweight / Избыточный вес"
        else:
            return "Obesity / Ожирение"
    else:
        return "Invalid gender"

def main():
    unit_system = input("Введите систему измерения (kg/cm или lb/in): ")
    height = float(input("Введите ваш рост: "))
    weight = float(input("Введите ваш вес: "))
    gender = input("Введите ваш пол (male/female): ")

    bmi = calculate_bmi(height, weight, gender)
    category = get_bmi_category(bmi, gender)

    print(f"Ваш ИМТ: {bmi:.2f}")
    print(f"Категория ИМТ: {category}")

if __name__ == "__main__":
    main()